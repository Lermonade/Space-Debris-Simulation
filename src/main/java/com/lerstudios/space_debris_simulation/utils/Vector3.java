package com.lerstudios.space_debris_simulation.utils;

public class Vector3 {
    public double x = 0.0;
    public double y = 0.0;
    public double z = 0.0;

    public Vector3() {}

    public Vector3(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
